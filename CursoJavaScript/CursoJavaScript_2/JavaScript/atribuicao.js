const a = 7;
let b = 3;

b += a // b = b + a 
console.log(b);

b -= 4 // b = b - 4
console.log(b);

b *= 2 // b = b * 2
console.log(b);

b /= 2 // b = b / 2
console.log(b);

b %= 2 // b = b % 2
console.log(b);

/*
* Atribuições são importantes e podem ser implementadas
* com alguma coisa a mais como uma operação 
* que agilize o código e economize sentenças
*/